/**
 * @param {number[]} arr1
 * @param {number} m
 * @param {number[]} nums2
 * @param {number} n
 * @return {void} Do not return anything, modify arr1 in-place instead.
 */
var merge = function (nums1, m, nums2, n) {
  let arr1 = nums1;
  let t = '';
  let s = '';
  if (n != 0) {
    s = arr1.join(',');
    t = '0'.repeat(n).split('').join(',');
  }

  arr1 = (arr1.join(',') + ',' + nums2.join(',')).split(',').sort();

  if (t != '') {
    //0,0,0,1,2,2,3,5,6
    s = arr1.join(',').replace(t + ',', '');
    //console.log(s);
    arr1 = s.split(',');
  }
  nums1 = arr1;

  return arr1;
};
